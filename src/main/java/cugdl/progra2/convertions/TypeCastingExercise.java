package cugdl.progra2.convertions;

public class TypeCastingExercise {
    public static void main(String[] args) {
        double puntoFlotante = 100.4;
        System.out.println("Double convertido a int: " + (int) puntoFlotante);

        int entero = 25;
        System.out.println("Int convertido a double: " + (double) entero);
    }
}
